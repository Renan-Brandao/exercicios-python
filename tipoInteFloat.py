# Tipos int e float

""" 
Tipos int
int -> Número inteiro - Representa qualquer número positivo ou negativo, int sem sinal é considerado positivo.
"""
print(11)#int
print(-11)#int
print(0)#int
"""
Tipo float
float-> Número com ponto flutuante(virgula), representa qualquer número, positivo ou negativo com ponto flutuante
O float sem sinal é considerado positivo.
"""
print(-1.1, 5.2,-40.5)#float
print(50.2)#float

"""
A função "Type"
"""

print(type('sas'))
print(type(6))
print(type(6.7))
