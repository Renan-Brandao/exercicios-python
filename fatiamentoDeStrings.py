"""
Fatiamento de strings

Fatiamento[i:f:p] [::]
Obs: A função len retorna a quantidade de caracteres da string
"""
variavel = 'Olá, meu nome é João, tenho 28 anos'
print(variavel[5: :2])
print(len(variavel)) # Função Len, para contagem de caracteres a contagem deve começar a partir de 1
print(variavel[::-34])